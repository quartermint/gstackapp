/**
 * Build context information for mock response
 */
function buildContextInfo(
  agent: AgentProfile,
  trustContext: TrustContext
): string {
  const parts: string[] = [
    `Agent: ${agent.name}`,
    `Trust Level: ${trustContext.level}`,
  ];

  if (agent.allowedTools.length > 0) {
    parts.push(`Available Tools: ${agent.allowedTools.join(', ')}`);
  } else {
    parts.push('Available Tools: None (read-only mode)');
  }

  if (trustContext.userId) {
    parts.push(`User: ${trustContext.userId}`);
  }

  return parts.join('\n');
}

/**
 * Build a mock response for development/testing
 */
function buildMockResponse(
  userMessage: string,
  agent: AgentProfile,
  contextInfo: string
): string {
  return `[STUB RESPONSE - Claude CLI not yet integrated]

Your message: "${userMessage.slice(0, 100)}${userMessage.length > 100 ? '...' : ''}"

Context:
${contextInfo}

This is a placeholder response. In production, this would be generated by Claude via the CLI with the following constraints:
- Max tokens: ${agent.maxTokens || 4096}
- Allowed tools: ${agent.allowedTools.length > 0 ? agent.allowedTools.join(', ') : 'none'}
- Profile: ${agent.description}`;
}

/**
 * Get the Claude CLI command arguments for an agent profile
 * (For future implementation)
 */
export function getClaudeCliArgs(
  agent: AgentProfile
): Record<string, string | number | boolean | string[]> {
  const args: Record<string, string | number | boolean | string[]> = {
    '--max-tokens': agent.maxTokens || 4096,
  };

  // Add tool restrictions
  if (agent.allowedTools.length === 0) {
    args['--no-tools'] = true;
  } else {
    args['--allowed-tools'] = [...agent.allowedTools];
  }

  // Add system prompt additions if specified
  if (agent.systemPromptAdditions) {
    args['--system-prompt-suffix'] = agent.systemPromptAdditions;
  }

  return args;
}
