/**
 * Claude CLI wrapper service (stub)
 *
 * Handles agent selection based on trust level and
 * execution of agent tasks via Claude CLI
 */

import {
  TrustContext,
  AgentProfile,
  AgentProfileId,
  ChatRequest,
  AGENT_PROFILE_DEFINITIONS,
  meetsTrustLevel,
} from '@mission-control/shared';

/**
 * Agent execution result
 */
export interface AgentExecutionResult {
  /** Agent response content */
  content: string;
  /** Token usage statistics */
  usage?: {
    inputTokens: number;
    outputTokens: number;
  };
  /** Whether the execution was successful */
  success: boolean;
  /** Error message if failed */
  error?: string;
}

/**
 * Select the appropriate agent profile based on trust level
 *
 * Rules:
 * 1. If a specific profile is requested, verify trust level allows it
 * 2. If requested profile not allowed, downgrade to highest allowed
 * 3. If no profile requested, use highest allowed for trust level
 *
 * @param trustContext - The trust context from request classification
 * @param requestedProfile - Optional requested agent profile
 * @returns The selected agent profile
 */
export function selectAgent(
  trustContext: TrustContext,
  requestedProfile?: AgentProfileId
): AgentProfile {
  // If a specific profile is requested, check if trust level allows it
  if (requestedProfile) {
    const profile = AGENT_PROFILE_DEFINITIONS[requestedProfile];
    if (profile && meetsTrustLevel(trustContext.level, profile.minTrustLevel)) {
      return profile;
    }
    // Requested profile not allowed - fall through to find best alternative
  }

  // Find the highest capability profile allowed for this trust level
  const profiles = Object.values(AGENT_PROFILE_DEFINITIONS);

  // Sort by capability (more tools = higher capability)
  const sortedProfiles = profiles
    .filter((p) => meetsTrustLevel(trustContext.level, p.minTrustLevel))
    .sort((a, b) => b.allowedTools.length - a.allowedTools.length);

  // Return highest capability allowed, or fallback to chat-readonly
  return sortedProfiles[0] || AGENT_PROFILE_DEFINITIONS['chat-readonly'];
}

